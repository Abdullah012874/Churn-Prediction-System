import React, { useEffect, useState } from 'react';
import { Bar } from 'react-chartjs-2';

const PredictionResult = ({ result, modelMetrics, setActivePage }) => {
  const [currentVal, setCurrentVal] = useState(0);

  const probPercent = result ? Math.round(result.probability * 100) : 0;

  useEffect(() => {
    let interval;
    if (result) {
      setCurrentVal(0);
      let val = 0;
      interval = setInterval(() => {
        if (val >= probPercent) {
          clearInterval(interval);
          return;
        }
        val++;
        setCurrentVal(val);
      }, 15);
    }
    return () => clearInterval(interval);
  }, [result, probPercent]);

  if (!result) return null;

  let color = '#111111';
  let msg = 'Stable customer profile.';
  let icon = 'fa-circle-check';

  if (result.risk_level === 'High') {
    color = '#111111';
    msg = 'Immediate retention action recommended.';
    icon = 'fa-circle-exclamation';
  } else if (result.risk_level === 'Medium') {
    color = '#666666';
    msg = 'Monitor customer activity closely.';
    icon = 'fa-circle-info';
  } else {
    color = '#E5E5E5';
    msg = 'Customer is satisfied and loyal.';
    icon = 'fa-circle-check';
  }

  // Model comparison charts
  const MODEL_ABBR = {
    'Logistic Regression': 'LR',
    'KNN': 'KNN',
    'Naive Bayes': 'NB',
    'SVM': 'SVM'
  };

  const selectedModel = result.selected_model || 'Best Model';
  const bestModel = modelMetrics?.best_model;
  const models = modelMetrics?.models ? Object.keys(modelMetrics.models) : [];
  
  const chartLabels = models.map(m => MODEL_ABBR[m] || m);
  const accuracies = models.map(m => modelMetrics.models[m].accuracy * 100);
  const precisions = models.map(m => modelMetrics.models[m].precision * 100);
  const recalls = models.map(m => modelMetrics.models[m].recall * 100);
  const f1s = models.map(m => modelMetrics.models[m].f1_score * 100);

  const getColors = () => models.map(m => {
    if (m === selectedModel) return '#111111';
    if (m === bestModel) return '#555555';
    return '#CCCCCC';
  });

  const commonOpts = {
    responsive: true,
    maintainAspectRatio: false,
    plugins: {
      legend: { display: false },
      tooltip: {
        callbacks: {
          label: (ctx) => {
            const modelName = models[ctx.dataIndex];
            let label = `${modelName}: ${ctx.parsed.y.toFixed(1)}%`;
            if (modelName === selectedModel) label += ' (Selected)';
            else if (modelName === bestModel) label += ' (Best)';
            return label;
          }
        }
      }
    },
    scales: {
      y: {
        min: 0, max: 100,
        grid: { color: '#F0F0F0' },
        ticks: { font: { size: 9 }, color: '#888', maxTicksLimit: 5 }
      },
      x: {
        grid: { display: false },
        ticks: {
          font: { size: 11, weight: '700' },
          color: '#333333'
        }
      }
    }
  };

  const createChartData = (label, dataArr) => ({
    labels: chartLabels,
    datasets: [{
      label,
      data: dataArr,
      backgroundColor: getColors(),
      borderRadius: 4
    }]
  });

  return (
    <section id="result" className="page active">
      <div className="result-layout">
        <div className="result-main">
          <div className="card result-card">
            <div className="result-header">
              <span className={`badge badge-${result.risk_level.toLowerCase()}`}>{result.risk_level} Risk</span>
              <h1>{result.prediction}</h1>
              <div className="model-badge-card" id="modelBadgeCard">
                <div className="model-badge-icon"><i className="fa-solid fa-microchip"></i></div>
                <div className="model-badge-text">
                  <div className="model-badge-label">Prediction Generated By</div>
                  <div className="model-badge-name">{selectedModel}</div>
                </div>
              </div>
            </div>

            <div className="analysis-visual">
              <div 
                className="circular-progress" 
                style={{ background: `conic-gradient(${color} ${currentVal}%, #F5F5F5 0%)` }}
              >
                <span className="progress-val">{currentVal}%</span>
              </div>
              <div className="status-msg">
                <i className={`fa-solid ${icon}`}></i>
                <span>{msg}</span>
              </div>
            </div>

            <div className="suggestions-section">
              <h5>Retention Strategies</h5>
              <div className="suggestions-list">
                {(result.suggestions || []).map((sugg, i) => (
                  <div key={i} className="suggestion-item">
                    <i className="fa-solid fa-check"></i><p>{sugg}</p>
                  </div>
                ))}
              </div>
            </div>
          </div>
        </div>

        <div className="result-side">
          <div className="section-title">
            <h3>Model Comparisons</h3>
          </div>
          {models.length > 0 && (
            <div className="comparison-grid">
              <div className="card small-chart">
                <h5>Accuracy</h5>
                <div className="mini-chart-box"><Bar data={createChartData('Accuracy %', accuracies)} options={commonOpts} /></div>
              </div>
              <div className="card small-chart">
                <h5>Precision</h5>
                <div className="mini-chart-box"><Bar data={createChartData('Precision %', precisions)} options={commonOpts} /></div>
              </div>
              <div className="card small-chart">
                <h5>Recall</h5>
                <div className="mini-chart-box"><Bar data={createChartData('Recall %', recalls)} options={commonOpts} /></div>
              </div>
              <div className="card small-chart">
                <h5>F1 Score</h5>
                <div className="mini-chart-box"><Bar data={createChartData('F1 Score %', f1s)} options={commonOpts} /></div>
              </div>
            </div>
          )}
          <button className="btn-secondary full-width" onClick={() => setActivePage('predict')}>
            <i className="fa-solid fa-arrow-left"></i> Run Another Test
          </button>
        </div>
      </div>
    </section>
  );
};

export default PredictionResult;
